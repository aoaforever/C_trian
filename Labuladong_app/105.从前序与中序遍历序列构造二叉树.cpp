/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
#include <vector>
#include <iostream>
#include <algorithm>
using namespace std;

class Solution {
public:
    TreeNode* buildTree(vector<int>& preorder, vector<int>& inorder) {
        return build(preorder,0,preorder.size()-1,inorder,0,inorder.size()-1);
    }
    TreeNode* build(vector<int>& p, int p_l,int p_r, vector<int>& i, int i_l, int i_r){
        if(p_l>p_r||i_l>i_r) return NULL;
        TreeNode* tree = new TreeNode(p[p_l]);
        int val = p[p_l];
        int index =i_l;
        for(;index<=i_r;index++){
            if(i[index]==val) break;
        }
        int left_len = index - i_l;
        // int right_len = i_r - index;
        tree->left = build(p, p_l+1 , p_l+left_len , i, i_l, index-1);
        tree->right = build(p , p_l+left_len+1 , p_r, i , index+1 , i_r);
        return tree;
    }
};
